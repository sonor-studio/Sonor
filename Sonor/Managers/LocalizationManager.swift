    public static let shared = LocalizationManager()
    @AppStorage("appLanguage") public var appLanguage: String = "en" {
        willSet {
            objectWillChange.send()
        }
    }
    private init() {
        if UserDefaults.standard.object(forKey: "appLanguage") == nil {
            let preferredLanguages = Locale.preferredLanguages
            let systemLang = preferredLanguages.first ?? "Unknown"
            let supportedLanguages = ["en", "pl", "de", "es", "fr", "it", "ja", "pt", "zh"]
            var detectedLang = "en"
            for lang in supportedLanguages {
                if systemLang.hasPrefix(lang) {
                    detectedLang = lang
                    break
                }
            }
            UserDefaults.standard.set(detectedLang, forKey: "appLanguage")
            self.appLanguage = detectedLang
        }
    }
